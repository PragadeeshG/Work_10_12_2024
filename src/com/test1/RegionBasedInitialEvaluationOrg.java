package com.test1;

public class RegionBasedInitialEvaluationOrg {
	private long initialEvaluationOrgCode;
	private String routineImpactAnalysys;
	private String initialEvaluationOrgRegion;
	private String initialEvaluationOrgLevel;
	private long metadataCode;
	private String moduledata;
	private String remarks;
	private String content;
	private String creationDate;
	private String modifiedDate;
	private String entityState;

	public RegionBasedInitialEvaluationOrg() {

	}

	public RegionBasedInitialEvaluationOrg(long initialEvaluationOrgCode, String routineImpactAnalysys,
			String initialEvaluationOrgRegion, String initialEvaluationOrgLevel, long metadataCode, String moduledata,
			String remarks, String content, String creationDate, String modifiedDate, String entityState) {
		super();
		this.initialEvaluationOrgCode = initialEvaluationOrgCode;
		this.routineImpactAnalysys = routineImpactAnalysys;
		this.initialEvaluationOrgRegion = initialEvaluationOrgRegion;
		this.initialEvaluationOrgLevel = initialEvaluationOrgLevel;
		this.metadataCode = metadataCode;
		this.moduledata = moduledata;
		this.remarks = remarks;
		this.content = content;
		this.creationDate = creationDate;
		this.modifiedDate = modifiedDate;
		this.entityState = entityState;
	}

	public long getInitialEvaluationOrgCode() {
		return initialEvaluationOrgCode;
	}

	public void setInitialEvaluationOrgCode(long initialEvaluationOrgCode) {
		this.initialEvaluationOrgCode = initialEvaluationOrgCode;
	}

	public String getRoutineImpactAnalysys() {
		return routineImpactAnalysys;
	}

	public void setRoutineImpactAnalysys(String routineImpactAnalysys) {
		this.routineImpactAnalysys = routineImpactAnalysys;
	}

	public String getInitialEvaluationOrgRegion() {
		return initialEvaluationOrgRegion;
	}

	public void setInitialEvaluationOrgRegion(String initialEvaluationOrgRegion) {
		this.initialEvaluationOrgRegion = initialEvaluationOrgRegion;
	}

	public String getInitialEvaluationOrgLevel() {
		return initialEvaluationOrgLevel;
	}

	public void setInitialEvaluationOrgLevel(String initialEvaluationOrgLevel) {
		this.initialEvaluationOrgLevel = initialEvaluationOrgLevel;
	}

	public long getMetadataCode() {
		return metadataCode;
	}

	public void setMetadataCode(long metadataCode) {
		this.metadataCode = metadataCode;
	}

	public String getModuledata() {
		return moduledata;
	}

	public void setModuledata(String moduledata) {
		this.moduledata = moduledata;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}

	public String getModifiedDate() {
		return modifiedDate;
	}

	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}

	public String getEntityState() {
		return entityState;
	}

	public void setEntityState(String entityState) {
		this.entityState = entityState;
	}

}
